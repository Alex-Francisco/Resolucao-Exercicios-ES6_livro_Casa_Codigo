/* Exercício 10 - E tem alguma diferença?
Diga, em poucas palavras, qual a diferença entre os métodos auxiliares forEach e map.
*/

/* Resposta: A diferença é que o forEach é usado quando queremos apenas iterar sobre os itens de um array, exibindo
esses valores. O uso da map já é válido quando queremos iterar um array fazendo alguma alteração nos valores, de 
forma que o resultado tenha a saída em um novo array, sem modificar o original */